from __future__ import annotations

import argparse
import re
import textwrap
from dataclasses import dataclass, field
from pathlib import Path
from typing import Dict, List, Tuple

Color = Tuple[float, float, float]


@dataclass
class ColormapEntry:
    """All variants of a single colormap family."""
    continuous: List[Color] = field(default_factory=list)
    discrete: Dict[int, List[Color]] = field(default_factory=dict)   # key = N (10,25,50,100)
    categorical: List[Color] = field(default_factory=list)


class ColormapConverter:
    DISCRETE_SIZES = (10, 25, 50, 100)

    def __init__(self, input_folder: str | Path,
                 header_path: str | Path,
                 cpp_path: str | Path,
                 include_path: str | None = None,
                 namespace: str = "Colormaps",
                 copyright_file: str | Path | None = None,
                 api_macro: str = "",
                 api_include: str = ""):
        self.input_folder = Path(input_folder)
        self.header_path = Path(header_path)
        self.cpp_path = Path(cpp_path)
        # The path used in #include "..." in the .cpp file
        self.include_path = include_path if include_path else f"\"{self.header_path.name}\""
        self.namespace = namespace
        self.copyright_header = self._load_copyright(copyright_file)
        self.api_macro = api_macro
        self.api_include = api_include
        self.entries: Dict[str, ColormapEntry] = {}

    @staticmethod
    def _block_comment(text: str, width: int = 100) -> str:
        """Wrap *text* in a bordered block comment, line-broken at *width* columns.

        Output style:
        /*****…*
         *
         * <wrapped lines>
         *
         ****…*/
        """
        border = "/" + "*" * (width - 1)
        close = " " + "*" * (width - 2) + "/"
        inner_width = width - len(" * ")

        lines: list[str] = []
        for paragraph in text.splitlines():
            paragraph = paragraph.strip()
            if not paragraph:
                lines.append(" *")
            else:
                wrapped = textwrap.wrap(paragraph, width=inner_width)
                for wl in wrapped:
                    lines.append(f" * {wl}")

        return border + "\n" + "\n".join(lines) + "\n" + close + "\n"

    @staticmethod
    def _load_copyright(path: str | Path | None) -> str:
        """Load an additional copyright header from a file."""
        if path is None:
            return ""
        p = Path(path)
        if not p.exists():
            print(f"Warning: copyright file not found: {p}")
            return ""
        return p.read_text(encoding='utf-8').rstrip()

